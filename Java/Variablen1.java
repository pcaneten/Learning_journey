public class Variablen1 {
	public static void main(String[] args) {
		
		//Variablendeklarationen
		byte bZahl;
		short sZahl;
		int iZahl;
		long lZahl;
		int fZahl;
		double dZahl;
		boolean bestanden;
		char zeichen;
		
		//Wertzuweisungen
		
		bZahl = 28;
		sZahl = -18453;
		iZahl = 4356576;
		lZahl = 345236577970L;
		fZahl = 4.37456678F;
		dZahl = 3645.564782;
		bestanden = true;
		zeichen = '%';
		
		// Ausgabe der Variablenwerte
		System.out.println("bZahl\t\t = \t" + bZahl);
		System.out.println("sZahl\t\t = \t" + sZahl);
		System.out.println("iZahl\t\t = \t" + iZahl);
		System.out.println("lZahl\t\t = \t" + lZahl);
		System.out.println("fZahl\t\t = \t" + fZahl);
		System.out.println("dZahl\t\t = \t" + dZahl);
		System.out.println("bestanden\t = \t" + bestanden);
		System.out.println("zeichen\t\t = \t" + zeichen);
	}
}
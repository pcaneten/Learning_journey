package variablen1;

public class Variablen1 {
	public static void main(String[] args) {
		
		//Variablendeklarationen
		byte bZahl;
		short sZahl;
		int iZahl;
		long lZahl;
		float fZahl;
		double dZahl;
		boolean bestanden;
		char zeichen;
		
		//Wertzuweisungen
		
		bZahl = 28;
		sZahl = -18453;
		iZahl = 4356576;
		lZahl = 345236577970L;
		fZahl = 4.37456678;
		dZahl = 3645.564782;
		bestanden = true;
		zeichen = '%';
		
		// Ausgabe der Variablenwerte
		System.out.println("bZahl=" + bZahl);
		System.out.println("sZahl=" + sZahl);
		System.out.println("iZahl=" + iZahl);
		System.out.println("lZahl=" + lZahl);
		System.out.println("fZahl=" + fZahl);
		System.out.println("dZahl=" + dZahl);
		System.out.println("bestanden=" + bestanden);
		System.out.println("zeichen=" + zeichen);
	}
}